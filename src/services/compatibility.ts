// Privé — Love Personality (Tier A redesign, 2026-05-24)
//
// Scientifically-grounded personality system replacing the previous
// 8-question MBTI-clone. Combines two validated instruments:
//
//   • Big Five / OCEAN  — short-form BFI-10
//     (Rammstedt & John, 2007, J. Research in Personality 41:203-212).
//     10 items, 2 per dimension, half reverse-keyed, 5-point Likert.
//
//   • Attachment Theory — Bartholomew & Horowitz Relationship Questionnaire
//     (1991). 4 items, one per style, each rated 5-point "how well does
//     this describe you". Primary style = highest rating; ties resolved
//     in favor of more relationally-functional styles.
//
// The user-facing reveal ("Your Love Personality") is generated per-user
// by Claude (see supabase/functions/ai-love-personality-reveal, Phase 3)
// — every reveal is unique, grounded in the real scores. Internally we
// surface Neuroticism as its inverse (Emotional Stability) so the brand
// language stays positive; scoring math still computes raw N first.

import type { AppLanguage } from '../domain'

// ── Types ───────────────────────────────────────────────────────────

/** 5-point Likert response: 1=Strongly disagree, 5=Strongly agree. */
export type LikertAnswer = 1 | 2 | 3 | 4 | 5

/** Continuous 0–100 scores for the five Big Five dimensions. */
export type BigFiveScores = {
  openness: number
  conscientiousness: number
  extraversion: number
  agreeableness: number
  /** Raw Neuroticism score. UI surfaces (100 - neuroticism) as Emotional Stability. */
  neuroticism: number
}

/** Adult attachment style — four categories from Bartholomew-Horowitz. */
export type AttachmentStyle = 'secure' | 'anxious' | 'avoidant' | 'disorganized'

/** Raw 1–5 ratings for each attachment description (one item per style). */
export type AttachmentRatings = {
  secure: LikertAnswer
  anxious: LikertAnswer
  avoidant: LikertAnswer
  disorganized: LikertAnswer
}

/** The user-facing reveal generated by Claude. Cached on the SelfProfile. */
export type LovePersonalityReveal = {
  archetypeName: string  // e.g., "Curious Anchor" — 2-3 words, poetic
  headline: string       // one resonant sentence
  description: string    // 3-4 short paragraphs
  strengths: string[]    // 3 short phrases
  growthEdges: string[]  // 2 short phrases (compassionate framing)
  language: 'en' | 'ro'
  generatedAt: string    // ISO timestamp
}

/** The full Love Personality stored on the profile. */
export type LovePersonality = {
  bigFive: BigFiveScores
  attachment: AttachmentStyle
  attachmentRatings: AttachmentRatings
  completedAt: string  // ISO timestamp
  reveal?: LovePersonalityReveal  // optional cached Claude output
}

/** A single quiz question shown to the user. */
export type PersonalityQuestion = {
  id: number
  /** What this item measures: a Big Five dimension or an attachment style. */
  measures:
    | { type: 'bigFive'; dimension: keyof BigFiveScores; reverse: boolean }
    | { type: 'attachment'; style: AttachmentStyle }
  /** Stem text shown above the Likert scale. */
  prompt: string
}

