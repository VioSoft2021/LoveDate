/** Number of questions in the assessment (10 Big Five + 4 attachment). */
export const PERSONALITY_QUESTION_COUNT = 14
export const BIG_FIVE_QUESTION_COUNT = 10
export const ATTACHMENT_QUESTION_COUNT = 4

// ── Scoring ─────────────────────────────────────────────────────────

const reverse = (x: LikertAnswer): number => 6 - x // 5-point reverse-key

const likertToScore0to100 = (avg: number): number =>
  // Map mean Likert (1..5) to 0..100. (avg - 1) * 25 → 0..100.
  Math.max(0, Math.min(100, Math.round((avg - 1) * 25)))

/**
 * Compute Big Five scores from the first 10 Likert answers.
 * Returns 0..100 per dimension. Missing/invalid input yields a neutral 50.
 */
export const bigFiveFromAnswers = (answers: LikertAnswer[]): BigFiveScores => {
  // For each BFI-10 item, get the raw rating; if absent → neutral 3 (Neutral).
  const a = (id: number): LikertAnswer => {
    const v = answers[id - 1]
    return v === 1 || v === 2 || v === 3 || v === 4 || v === 5 ? v : 3
  }
  const avg = (x: number, y: number) => (x + y) / 2

  // BFI-10 dimension recipes (Rammstedt & John, 2007):
  //   Openness          = avg( reverse(Q5),  Q10 )
  //   Conscientiousness = avg( reverse(Q3),  Q8  )
  //   Extraversion      = avg( reverse(Q1),  Q6  )
  //   Agreeableness     = avg( Q2,  reverse(Q7) )
  //   Neuroticism       = avg( reverse(Q4), Q9  )

  return {
    openness:          likertToScore0to100(avg(reverse(a(5)),  a(10))),
    conscientiousness: likertToScore0to100(avg(reverse(a(3)),  a(8))),
    extraversion:      likertToScore0to100(avg(reverse(a(1)),  a(6))),
    agreeableness:     likertToScore0to100(avg(a(2),           reverse(a(7)))),
    neuroticism:       likertToScore0to100(avg(reverse(a(4)),  a(9))),
  }
}

/**
 * Determine primary attachment style from the four ratings.
 * Highest rating wins; ties broken in order: secure > anxious > avoidant > disorganized
 * (favouring the more relationally-functional style on equal scores).
 */
export const attachmentFromRatings = (ratings: AttachmentRatings): AttachmentStyle => {
  const order: AttachmentStyle[] = ['secure', 'anxious', 'avoidant', 'disorganized']
  let best: AttachmentStyle = 'secure'
  let bestScore = -Infinity
  for (const style of order) {
    if (ratings[style] > bestScore) {
      bestScore = ratings[style]
      best = style
    }
  }
  return best
}

/**
 * Build the full LovePersonality struct from the 14 Likert answers.
 * Indices 0-9 are BFI-10; indices 10-13 are the four attachment items
 * (in order: secure, anxious, avoidant, disorganized).
 */
export const lovePersonalityFromAnswers = (
  answers: LikertAnswer[],
): LovePersonality | null => {
  if (!Array.isArray(answers) || answers.length !== PERSONALITY_QUESTION_COUNT) {
    return null
  }
  const bigFive = bigFiveFromAnswers(answers.slice(0, BIG_FIVE_QUESTION_COUNT))
  const attachmentRatings: AttachmentRatings = {
    secure: answers[10],
    anxious: answers[11],
    avoidant: answers[12],
    disorganized: answers[13],
  }
  return {
    bigFive,
    attachment: attachmentFromRatings(attachmentRatings),
    attachmentRatings,
    completedAt: new Date().toISOString(),
  }
}

// ── Compatibility ──────────────────────────────────────────────────

/**
 * 4×4 attachment compatibility matrix from dyadic attachment research.
 * Secure pairings do best; anxious-avoidant the classic doomed pair.
 * Values are 0–100. Symmetric — order doesn't matter.
 */
const ATTACHMENT_COMPAT: Record<AttachmentStyle, Record<AttachmentStyle, number>> = {
  secure: {        secure: 95, anxious: 80, avoidant: 75, disorganized: 70 },
  anxious: {       secure: 80, anxious: 55, avoidant: 35, disorganized: 45 },
  avoidant: {      secure: 75, anxious: 35, avoidant: 50, disorganized: 45 },
  disorganized: {  secure: 70, anxious: 45, avoidant: 45, disorganized: 40 },
}

const BIG_FIVE_KEYS: (keyof BigFiveScores)[] = [
  'openness', 'conscientiousness', 'extraversion', 'agreeableness', 'neuroticism',
]

/**
 * Cosine similarity of two Big Five vectors, rescaled to 0–100.
 * Big Five similarity correlates positively (modestly) with relationship
 * satisfaction; see Luo & Klohnen 2005 and meta-analyses since.
 */
const bigFiveSimilarity = (a: BigFiveScores, b: BigFiveScores): number => {
  let dot = 0
  let magA = 0
  let magB = 0
  for (const k of BIG_FIVE_KEYS) {
    dot += a[k] * b[k]
    magA += a[k] * a[k]
    magB += b[k] * b[k]
  }
  if (magA === 0 || magB === 0) return 50
  const cos = dot / (Math.sqrt(magA) * Math.sqrt(magB))
  // Cosine on positive vectors is in [0,1]; rescale to a usable range.
  // Map [0.7, 1.0] → [40, 95] (typical observed range; complete dissimilarity
  // is rare in real personality data).
  return Math.max(0, Math.min(100, Math.round((cos - 0.7) / 0.3 * 55 + 40)))
}

/**
 * Compatibility score (0–100) between two Love Personalities.
 * Blends:
 *   55% Big Five cosine similarity
 *   45% Attachment dyadic compatibility (lookup matrix)
 * Returns 50 when data is missing for either side.
 */
export const compatibilityFromLovePersonality = (
  mine: LovePersonality | null | undefined,
  theirs: LovePersonality | null | undefined,
): number => {
  if (!mine || !theirs) return 50
  return compatibilityFromBigFiveAttachment(
    mine.bigFive,
    mine.attachment,
    theirs.bigFive,
    theirs.attachment,
  )
}

/**
 * Same blend as compatibilityFromLovePersonality but works on the raw
 * public-derived fields stored on remote Profile rows (bigFive +
 * attachmentStyle), where we don't have the local LovePersonality
 * wrapper / reveal text. Returns 50 if either side is missing data.
 */
export const compatibilityFromBigFiveAttachment = (
  mineBigFive: BigFiveScores | null | undefined,
  mineAttachment: AttachmentStyle | null | undefined,
  theirsBigFive: BigFiveScores | null | undefined,
  theirsAttachment: AttachmentStyle | null | undefined,
): number => {
  if (!mineBigFive || !mineAttachment || !theirsBigFive || !theirsAttachment) return 50
  const b5 = bigFiveSimilarity(mineBigFive, theirsBigFive)
  const att = ATTACHMENT_COMPAT[mineAttachment][theirsAttachment]
  return Math.max(1, Math.min(99, Math.round(b5 * 0.55 + att * 0.45)))
}
