// Phone-only bottom navigation. Desktop hides this via CSS
// (`.mobile-tab-bar { display: none }` outside the phone @media block).
// All 7 destinations show as icons; labels are visually hidden on
// narrow widths via the screen-reader-friendly `<span>` pattern.
export const MobileTabBar: React.FC<MobileTabBarProps> = ({
  items,
  currentScreen,
  onNavigate,
}) => {
  return (
    <nav className="mobile-tab-bar" aria-label="Primary navigation">
      {items.map((tab) => (
        <button
          key={tab.key}
          type="button"
          className={currentScreen === tab.key ? 'active' : ''}
          onClick={() => onNavigate(tab.key)}
        >
          {ICON_BY_KEY[tab.key]}
          <span>{tab.label}</span>
          {tab.badge && tab.badge > 0 ? (
            <span className="tab-badge">{tab.badge}</span>
          ) : null}
        </button>
      ))}
    </nav>
  )
}
