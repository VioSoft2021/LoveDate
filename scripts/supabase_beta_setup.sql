drop policy if exists "beta_invites_read_active" on public.beta_invites;
create policy "beta_invites_read_active"
on public.beta_invites
for select
to anon, authenticated
using (active = true and (expires_at is null or expires_at > now()) and uses_left > 0);

drop policy if exists "profiles_public_read" on public.profiles;
create policy "profiles_public_read"
on public.profiles
for select
to anon, authenticated
using (is_active = true);

drop policy if exists "user_settings_owner_select" on public.user_settings;
