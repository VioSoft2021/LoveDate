-- LoveDate beta bootstrap (run once in Supabase SQL editor)
-- Safe to re-run; uses IF NOT EXISTS and idempotent inserts.

create extension if not exists pgcrypto;

create table if not exists public.beta_invites (
  id uuid primary key default gen_random_uuid(),
  code text not null unique,
  active boolean not null default true,
  expires_at timestamptz,
  uses_left integer not null default 1 check (uses_left >= 0),
  created_at timestamptz not null default now()
);

create table if not exists public.profiles (
  id bigint generated by default as identity primary key,
  name text not null,
  age integer not null check (age >= 18 and age <= 99),
  city text not null,
  vibe text not null default 'Good energy',
  bio text not null default '',
  interests text[] not null default '{}',
  palette text[] not null default array['#141937', '#252d5c'],
  photos text[] not null default '{}',
  gender text not null check (gender in ('Woman', 'Man', 'Non-binary')),
  distance_km integer not null default 10,
  verified boolean not null default false,
  relationship_goal text not null default 'Long-term'
    check (relationship_goal in ('Long-term', 'Short-term', 'Friends', 'Figuring it out')),
  zodiac text not null default 'Libra',
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.user_settings (
  user_id uuid primary key references auth.users (id) on delete cascade,
  push_notifications boolean not null default true,
  email_notifications boolean not null default true,
  private_mode boolean not null default false,
  updated_at timestamptz not null default now()
);

-- Per-user self-profile (the user's own dating profile, distinct from the
-- public.profiles swipe pool). Stored as JSONB so the schema can evolve
-- without migrations every time we add a field.
create table if not exists public.user_profiles (
  user_id uuid primary key references auth.users (id) on delete cascade,
  profile_data jsonb not null,
  updated_at timestamptz not null default now()
);

create table if not exists public.user_preferences (
  user_id uuid primary key references auth.users (id) on delete cascade,
  min_age integer not null default 21,
  max_age integer not null default 39,
  city text not null default '',
  interest text not null default '',
  include_reviewed boolean not null default false,
  updated_at timestamptz not null default now()
);

create table if not exists public.chat_events (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users (id) on delete cascade,
  match_name text not null,
  outgoing_text text not null,
  generated_reply text not null,
  created_at timestamptz not null default now()
);

